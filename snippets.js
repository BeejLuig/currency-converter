const replaceCurrencyInDocument = () => {
  Array.from(document.body.querySelectorAll('*'))
    .filter(x => x.childElementCount === 0 && /€/.test(x.textContent))
    .forEach(x => {
      x.textContent = x.textContent.replace(
        /€(\d+)/, 
        (match, p1) => `$${(Number(p1) * 1.14225).toFixed(2)}`
      )
    });
}

const calculateConversion = (base, currency) =>
  (1 / base) * (currency)

